    private void themMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_themMouseClicked
        // TODO add your handling code here:
        // Nếu madv nhập vào trùng với madv đã có trong csdl in ra thông báo
        try {
            conn = new connectDB().getConnection();
            String sql = "SELECT COUNT(*) FROM DichVu WHERE maDV = ?";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, jTextField1.getText());
            ResultSet rs = ps.executeQuery();
            if(jTextField2.getText().equals("")||jTextField3.getText().equals("")||jTextField4.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Vui lòng nhập đầy đủ thông tin");
            } else {
                sql = "{call InsertDichVu(?, ?, ?)}";
                CallableStatement cs = conn.prepareCall(sql);
                cs.setString(1, jTextField2.getText());
                cs.setString(2, jTextField3.getText());
                cs.setString(3, jTextField4.getText());
                cs.executeUpdate();
                inTable();
                jTextField1.setText("");
                jTextField2.setText("");
                jTextField3.setText("");
                jTextField4.setText("");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

        
    }//GEN-LAST:event_themMouseClicked

