    private void intableHoadon(){
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        try {
            conn = new connectDB().getConnection();
            SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");
            String sql = "SELECT * FROM HoaDon";
            PreparedStatement ps = conn.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                Vector row = new Vector();
                row.add(rs.getString("maHD").trim());
                row.add(rs.getString("maNV").trim());
                row.add(rs.getString("maKH").trim());
                row.add(rs.getString("maPT").trim());
                //nếu maHDDV là NULL thì trả về "0"
                if (rs.getString("maHDDV") == null) {
                    row.add("0");
                } else {
                    row.add(rs.getString("maHDDV").trim());
                }
                row.add(rs.getString("tongTien").trim());
                row.add(rs.getString("tinhtrangTT").trim());
                if (rs.getString("ngaythanhtoan") == null){
                    row.add("N/A");
                } else {
                    row.add(sdf.format(rs.getDate("ngaythanhtoan")));
                }
                model.addRow(row);
                }
            jTable1.setModel(model);
        } catch (Exception e) {
            //in thông báo
            JOptionPane.showMessageDialog(null, "Lỗi: " + e);
        }
    }

