    private void tongTien(){
        // tongTien trong bảng HoaDon sẽ bằng tinhtien trong bảng PhieuThue + tongTienDV trong bảng HoaDonDichVu
        try {
            conn = new connectDB().getConnection();
            String sql = "SELECT * FROM PhieuThue WHERE maPT = ?";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, jTextField3.getText());
            ResultSet rs = ps.executeQuery();
            float tongTien = 0;
            while (rs.next()) {
                tongTien = rs.getFloat("tinhtien");
            }
            String maHDDV = jTextField2.getText();
            if (!maHDDV.isEmpty()) {
                String sql1 = "SELECT * FROM HoaDonDichVu WHERE maHDDV = ?";
                PreparedStatement ps1 = conn.prepareStatement(sql1);
                ps1.setString(1, maHDDV);
                ResultSet rs1 = ps1.executeQuery();
                while (rs1.next()) {
                    tongTien += rs1.getFloat("tongTienDV");
                }
            }
            tongtien.setText(String.valueOf(tongTien));
        } catch (Exception e) {
            // Show error message
            JOptionPane.showMessageDialog(null, "Lỗi: " + e);
        }
        
    }

