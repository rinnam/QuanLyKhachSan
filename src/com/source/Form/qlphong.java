    private void inTable(){
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        try {
            conn = new connectDB().getConnection();
            String sql = "SELECT * FROM Phong";
            PreparedStatement ps = conn.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("maphong").trim());
                v.add(rs.getString("loaiphong").trim());
                v.add(rs.getString("hangphong").trim());
                v.add(rs.getFloat("gia1giodau"));
                v.add(rs.getFloat("giacacgiosau"));
                v.add(rs.getFloat("giatheongay"));
                v.add(rs.getString("tinhtrang").trim());
                model.addRow(v);
            }
            jTable1.setModel(model);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    
