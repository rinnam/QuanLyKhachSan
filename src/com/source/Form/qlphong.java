            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, jComboBox1.getSelectedItem().toString());
            ps.setString(2, jComboBox2.getSelectedItem().toString());
            ps.setString(3, jTextField2.getText());
            ps.setString(4, jTextField4.getText());
            ps.setString(5, jTextField3.getText());
            ps.setString(6, jComboBox3.getSelectedItem().toString());
            ps.setString(7, jTextField1.getText());
