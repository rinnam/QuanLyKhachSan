    private void jPanel3MouseClicked(java.awt.event.MouseEvent evt) {
        try {
            conn = new connectDB().getConnection();
        } catch (Exception e) {
            e.printStackTrace();
        }
        String sql = "select * from NhanVien where taikhoan = ? and matkhau = ?";
        try{
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, jTextField1.getText());
            ps.setString(2, jPasswordField1.getText());
            ResultSet rs = ps.executeQuery();
            if(jTextField1.getText().equals("") || jPasswordField1.getText().equals("")){
                JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin");
            }
            else if(rs.next()){
                //cập nhập cột trangthai trong bảng NhanVien chổ nhân viên vừa đăng nhập
                String sql1 = "update NhanVien set trangthai = 1 where taikhoan = ?";
                PreparedStatement ps1 = conn.prepareStatement(sql1);
                ps1.setString(1, jTextField1.getText());
                ps1.executeUpdate();

                Main m = new Main();
                m.setVisible(true);
                m.setLocationRelativeTo(null);
                this.dispose();   
            }
            else {
                JOptionPane.showMessageDialog(this, "Tài khoản hoặc mật khẩu không đúng");
            }
        }catch(Exception e){
            e.printStackTrace();
        }
    }
        
        
                                   
    
